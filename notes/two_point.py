from typing import List


def two_point(array: List):
    n = len(array)
    i, j = 0, n - 1
    # init two point
    while i < j:
        # terminator
        if True:
            # Meet Left side move
            i += 1
        if True:
            # Meet right side move
            j -= 1
    # Return the requirement
    return array
